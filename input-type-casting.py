# Int type-casting

a = int(input("Enter Your Age: "))
b = int(input("Enter Your Father's Age: "))
print("Your age is: ", a)
print("The type of the variable a is : ", type(a))
print("Your father's age is: ", b)
print("The type of the variable b is : ", type(b))
print("The sum of your and your father's age is: ", a+b)

