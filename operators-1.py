# Arithmetical Operators
print("Arithmetical Operators")

a = 10
b = 5

print(a+b)
print(a-b)
print(a*b)
print(a/b)

c = 7
d = 2

print(c%d)
print(c/d)
print(c//d)
print(c**d)

# Relational Operators
print("Relational Operators")

e = 10
f = 15

print(e==f)
print(e!=f)
print(e > f)
print(e >= f)
print(e <= f)
print(e < f)

# Logical Operators
print("Logical Operators")

g = 10
h = 20
i = 10
j = 20

print(g==h and h==i)
print(g==i and h==g)
print(g==i and h==j)

print(g==h or i==j)
print(g==h or h==j)

print(g==h)
print(not g==h)